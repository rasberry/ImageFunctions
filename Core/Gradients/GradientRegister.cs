	static GimpGGRGradient LoadGimpGradient(string inputData)
	{
		using var stream = new MemoryStream();
		var data = Encoding.UTF8.GetBytes(inputData);
		stream.Write(data);
		stream.Position = 0;

		var gradient = new GimpGGRGradient(stream);
		return gradient;
	}

